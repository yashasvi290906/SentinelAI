"""
Attack-Stage-Aware Correlation Engine for SentinelAI.
Groups related alerts into incidents based on kill chain progression,
not just alert counts.

Real SOC correlation considers:
- Time proximity (events within a window)
- Host/IP association (same source or target)
- Attack stage progression (recon → exploit → lateral movement → exfil)
- Alert severity escalation
"""

